package model;

public class Road {
    public Integer roadCount;
    public Integer roadLength;

    public Integer getRoadCount() {
        return roadCount;
    }

    public void setRoadCount(Integer roadCount) {
        this.roadCount = roadCount;
    }

    public Integer getRoadLength() {
        return roadLength;
    }

    public void setRoadLength(Integer roadLength) {
        this.roadLength = roadLength;
    }
}
